    public void displyDataIntoTable() {
        clearTable();
        TransactionDao tra = new TransactionDaoImpli();
        ClientDao cli = new ClientDaoImpli();
        List<Transaction> list = tra.getList();
        DefaultTableModel model = (DefaultTableModel) jTableDisplay.getModel();
        Object[] cols = new Object[9];
        for (int i = 0; i < list.size(); i++) {
            cols[0] = list.get(i).getId();
            cols[1] = list.get(i).getTotalAmount();
            cols[2] = list.get(i).getPaidAmount();

            cols[3] = list.get(i).getPaymentType();
            cols[4] = list.get(i).getPurposeOfPayment();
            cols[5] = list.get(i).getPaymentDate();
            cols[6] = list.get(i).getPaymentPlace();
            cols[7] = list.get(i).getNote();
            Client client = cli.getClientBYId(list.get(i).getClient().getId());
            cols[8] = client.getName();
            model.addRow(cols);
        }
    }

    public void displayDataIntoComboBox() {
        List<Client> list = new ClientDaoImpli().getList();
        jComboBoxClientName.addItem("Select A Company");
        for (Client c : list) {
            jComboBoxClientName.addItem(c.getName());
        }
    }

