    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        // insert Button
        TransactionDao tra = new TransactionDaoImpli();
        ClientDao cli = new ClientDaoImpli();
        double totalAmmouny = Double.parseDouble(jTextFieldTotalAmount.getText());
        double paidAmmouny = Double.parseDouble(jTextFieldAmount.getText());

        String paymentType = jComboBoxType.getItemAt(jComboBoxType.getSelectedIndex());
        Date date = new Date(System.currentTimeMillis());
        String purposeofAmount = jTextFieldPurpose.getText();
        String paymentPlace = jTextFieldPlace.getText();
        String note = jTextAreaNote.getText();
        Client client = cli.getClientBYName(jComboBoxClientName.getItemAt(jComboBoxClientName.getSelectedIndex()));

        Transaction trans = new Transaction(totalAmmouny, paidAmmouny, paymentType, purposeofAmount, date, paymentPlace, note, client);
        tra.save(trans);
        displyDataIntoTable();
        JOptionPane.showMessageDialog(null, "Data is inserted successfully into taransaction!");
        ///insert data into summary
        try {
            //transaction(totalAmount ,paidAmount,dueAmount,purposeOfPayment,paymentType,paymentDate,payment_place,note,c_id
            double due_amount = totalAmmouny - paidAmmouny;
            //totalAmount double,paidAmount double,dueAmount double, purposeOfPayment varchar(20), paymentType varchar(20),paymentDate date, payment_place varchar(20),note varchar(20),c_id int(20),foreign key (c_id) references client(id))
            Summary sum = new Summary(totalAmmouny, paidAmmouny, due_amount, paymentType, purposeofAmount, date, paymentPlace, note, client);
            new SummaryDaoImpli().save(sum);
            JOptionPane.showMessageDialog(null, "Data is inserted successfully into summary table!");
            displyDataIntoTable();
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton7ActionPerformed

